    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRowCount() == 1) {
            String productid = txtproductid.getText();
            String productname = txtproductname.getText();
            String bcatagory = cmbcatergory.getSelectedItem().toString();
            String colour = txtcolour.getText();
            String quantity = txtquantity.getText();
            String price = txtprice.getText();
            
            model.setValueAt(productid, jTable1.getSelectedRow(), 0);
            model.setValueAt(productname, jTable1.getSelectedRow(),1);
            model.setValueAt(bcatagory, jTable1.getSelectedRow(),2);
            model.setValueAt(colour,jTable1.getSelectedRow(),3);
            model.setValueAt(quantity, jTable1.getSelectedRow(), 4);
            model.setValueAt(price, jTable1.getSelectedRow(), 5);
            
            
            
            JOptionPane.showMessageDialog(this, "Updated Successfuly!");
            
        
            
        } else {
            if(jTable1.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "Row is blank");
            }else{
            JOptionPane.showMessageDialog(this, "Please select a row to update");
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

