    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String productid = txtproductid.getText();
        String productname = txtproductname.getText();
        String bcatagory = cmbcatergory.getSelectedItem().toString();
        String colour = txtcolour.getText();
        String quantity = txtquantity.getText();
        String price = txtprice.getText();

        try {

            FileWriter writter1 = new FileWriter("items.txt", true);
            writter1.write("Product ID : " + productid + " \n"
                    + "Product Name : " + productname + " \n"
                    + "Bag Catagory : " + bcatagory + " \n"
                    + "Product Colour : " + colour + " \n"
                    + "Product Quantity : " + quantity + " \n"
                    + "Product Price : " + price+ " \n\n");
            writter1.write(System.getProperty("line.separator"));
            writter1.close();
            
             DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(new Object[]{txtproductid.getText(), txtproductname.getText(),
           cmbcatergory.getSelectedItem().toString(),
            txtcolour.getText(),
            txtquantity.getText(),
            txtprice.getText(),});
            
            JOptionPane.showMessageDialog(null, "Paws Store Updated Succesfully !");
            
            txtproductid.setText("");
        txtproductname.setText("");
        txtcolour.setText("");
        txtquantity.setText("");
        txtprice.setText("");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Store not updates", "paws Store", JOptionPane.ERROR_MESSAGE);
        }

       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if (jTable1.getSelectedRowCount() == 1) {
            int c = JOptionPane.showConfirmDialog(this, "Are you sure to delete this item ?","paws store",JOptionPane.YES_NO_OPTION);
            if(c==0){
            model.removeRow(jTable1.getSelectedRow());
            JOptionPane.showMessageDialog(this, "Item deleted successfuly !");
            }
            
        } else {
            JOptionPane.showMessageDialog(this, "Select a row to delete");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

